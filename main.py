app = FastAPI()

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["POST", "OPTIONS"],
    allow_headers=["*"],
)

class VerifyRequest(BaseModel):
    token: str

@app.post("/verify")
async def verify(req: VerifyRequest):
    try:
        payload = jwt.decode(
            req.token,
            PUBLIC_KEY,
            algorithms=["RS256"],
            issuer=ISSUER,
            audience=AUDIENCE,
        )

        return {
            "valid": True,
            "email": EMAIL,
            "sub": payload["sub"],
            "aud": payload["aud"]
        }

    except (ExpiredSignatureError, JWTError):
        raise HTTPException(
            status_code=401,
            detail={"valid": False}
        )